Widget buildIcon(IconData icon, {Color? color, double size = 24}) {
  return Icon(icon, color: color, size: size);
}

Widget buildSvgIcon(
  String assetName, {
  Color? color,
  double size = 24,
  BoxFit fit = BoxFit.contain,
}) {
  return SvgPicture.asset(
    assetName,
    width: size,
    height: size,
    fit: fit,
    colorFilter: color == null
        ? null
        : ColorFilter.mode(color, BlendMode.srcIn),
  );
}
